package com.huawei.nb.coordinator.helper.verify;

public class VerifyException extends Exception {
    private int code;
    private String msg;

    public VerifyException(int code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public int getCode() {
        return this.code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return this.msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
